import Foundation

public extension TimeInterval{
  public static let secondsOfMinute: TimeInterval = 60
  public static let secondsOfHour: TimeInterval = 60 * 60
  public static let secondsOfDay: TimeInterval = 60 * 60 * 24
  public static let secondsOfWeek: TimeInterval = 60 * 60 * 24 * 7
}
